package com.kh.constant;

public class Constant {

	// *상수
	//변하지 않는 값(수학 익힘책)	
	public static void main(String[] args) {
			
		// 정수(양수,음수)
		System.out.println(3); // 숫자
		System.out.println('3'); //문자     자료형(Data type)
		System.out.println("3");  //문자열
		System.out.println(-3);
		// print:출력 후 줄바꿈X , println:출력 후 줄바꿈
		
		// 실수(.)
		System.out.println(0.8);
		System.out.println(-0.8);
		// 문자는"" , 숫자는 ""X
		
		// (단일)문자, 문자열
		System.out.println('금');
		System.out.println("금 1000돈");
		
		// 논리갑(true, false)
		System.out.println(true);
		System.out.println(false);
		
		
	}
	
}
